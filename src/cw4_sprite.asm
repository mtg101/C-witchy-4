





SPRITE_INIT
    ; disable sprite while we setup
    lda     #$00
    sta     SPR_ENABLE


    ; point to sprites
    lda     #(witch_sprite_broom_back / 64)
    sta     SPR_PTR0

    lda     #(witch_sprite_witch / 64)
    sta     SPR_PTR1

    lda     #(witch_sprite_broom_front / 64)
    sta     SPR_PTR2

    ; sprite colours
    lda     #BLACK
    sta     SPR0_COLOR
    sta     SPR1_COLOR
    sta     SPR2_COLOR

    ; sprite locations
    lda     #100
    sta     SPR0_X
    lda     #150
    sta     SPR0_Y

    lda     #100 + SPR_WIDTH
    sta     SPR1_X
    lda     #150
    sta     SPR1_Y

    lda     #100 + SPR_WIDTH*2
    sta     SPR2_X
    lda     #150
    sta     SPR2_Y

    ; enable 3 sprites
    lda     #%00000111
    sta     SPR_ENABLE

    rts     


