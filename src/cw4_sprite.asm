





SPRITE_INIT
    ; disable sprite while we setup
    lda     #$00
    sta     SPR_ENABLE


    ; point to sprites
    lda     #(witch_sprite_left / 64)
    sta     SPR_PTR0

    lda     #(witch_sprite_right / 64)
    sta     SPR_PTR1

    ; sprite colours
    lda     #BLACK
    sta     SPR0_COLOR
    sta     SPR1_COLOR

    ; sprite locations
    lda     #100
    sta     SPR0_X
    lda     #150
    sta     SPR0_Y

    lda     #100 + SPR_WIDTH
    sta     SPR1_X
    lda     #150
    sta     SPR1_Y

    ; enable sprites
    lda     #%00000011
    sta     SPR_ENABLE

    rts     


